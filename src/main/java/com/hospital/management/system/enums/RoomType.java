package com.hospital.management.system.enums;

/**
 * Enum representing the different types of hospital rooms.
 */
public enum RoomType {
    /**
     * Normal rooms are for patients who need routine medical care,
     * such as observation, medication administration,
     * and recovery from minor illnesses or procedures.
     */
    NORMAL,

    /**
     * Surgical rooms are where surgeries and other invasive procedures are performed.
     * These rooms provide a sterile environment designed to minimize infection risks.
     */
    SURGICAL,

    /**
     * ICU rooms are for patients who are critically ill or have life-threatening conditions.
     * These patients require constant, round-the-clock monitoring and specialized care.
     */
    ICU,
    
}
